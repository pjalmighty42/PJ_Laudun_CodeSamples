    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        try {
            // TODO add your handling code here:
            //deletes the current row
            result.deleteRow();
            
            //Close off and comit changes to our DB
            state.close();
            result.close();
                    
            //Load SQL elements into a ResultsSet
            state = conn.createStatement(ResultSet.TYPE_SCROLL_INSENSITIVE, 
                    ResultSet.CONCUR_UPDATABLE);
            String SQL = "SELECT * FROM WORKERS";
            result = state.executeQuery(SQL);
            
            //Move cursor and get the 1st record
            result.next();
            int colID = result.getInt("ID");
            String id = Integer.toString(colID);
            String firstN = result.getString("FIRST_NAME");
            String lastN = result.getString("LAST_NAME");
            String job = result.getString("JOB_TITLE");
            
            //Display the 1st record in the form fields
            txtID.setText(id);
            txtFirstName.setText(firstN);
            txtLastName.setText(lastN);
            txtJobTitle.setText(job);
            
            JOptionPane.showMessageDialog(Workers.this, "Record Deleted!");
                    
        } 
        catch (SQLException e) 
        {
            JOptionPane.showMessageDialog(Workers.this, e.getMessage());
        }
        
    }//GEN-LAST:event_btnDeleteActionPerformed

