    private void btnNextActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnNextActionPerformed
        // TODO add your handling code here:
        try{
            //Show next record, else end of file, show previous
            if(result.next())
            {
                 int colID = result.getInt("ID");
                String id = Integer.toString(colID);
                String firstN = result.getString("FIRST_NAME");
                String lastN = result.getString("LAST_NAME");
                String job = result.getString("JOB_TITLE");

                //Display the 1st record in the form fields
                txtID.setText(id);
                txtFirstName.setText(firstN);
                txtLastName.setText(lastN);
                txtJobTitle.setText(job);
            }
            else
            {
                result.previous();
                JOptionPane.showMessageDialog(Workers.this, "End of File");
            }
        }
        catch(SQLException e){
            JOptionPane.showMessageDialog(Workers.this, e.getMessage());
        }
    }//GEN-LAST:event_btnNextActionPerformed

